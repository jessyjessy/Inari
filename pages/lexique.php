<button id="show1">Show</button>

<p id="para1" style="display:none">
Symbole de la caste des samouraïs, le katana est un sabre de plus de 60 cm.<br/>
Il est porté glissé dans la ceinture, tranchant dirigé vers le haut à la ceinture du coté gauche.<br/>
L'ensemble wakizashi-katana s'appelle le daisho.<br/>
Certaines périodes de l'histoire japonaise étant plus calmes, le katana avait plus un rôle d'apparat que d'arme réelle. <br/>
Le katana est une arme de taille et d'estoc.<br/>
</p>

<h2>Nunchaku</h2>

<button id="hide2">Hide</button>
<button id="show2">Show</button>

<p id="para2">Le nunchaku est un fléau à deux branches utilisé comme arme dans différentes écoles et disciplines martiales d'origine asiatique.<br/>
La dénomination japonaise « nunchaku » est utilisée dans les kobudō de l'archipel Okinawa. <br/>
Le terme « long-gian » est employé dans les arts martiaux vietnamiens.</p>



<h2>Saï</h2>


<button id="hide3">Hide</button>
<button id="show3">Show</button>

<p id="para3">Le sai est une arme asiatique dont l'origine est mal définie.<br/>
On pense qu'il dérive peut-être d'un outil agricole pour planter le riz, ou encore comme une pique à fruits, ou comme instrument de mesure. <br/>
Il a été principalement utilisé dans l'archipel d'Okinawa par la police les officiers japonais utilisant le jutte — ou jitte.<br/>
</p>

<h2>Shuriken</h2>

<button id="hide4">Hide</button>
<button id="show4">Show</button>


<p id="para4">Le shuriken est une arme traditionnelle japonaise de lancer, qui est utilisée dans le cadre du shurikenjutsu1. <br/>
Cet art martial fait partie intégrante de la formation des ninja.<br/>
Contrairement à certaines idées reçues, cette arme est relativement peu efficace, et donc peu utilisée, en combat direct. <br/>
Elle est plutôt utilisée afin de distraire l’adversaire ou dans des embuscades où la rapidité d’attaque est essentielle.<br/>
</p>


<h2>Tanto</h2>

<button id="hide5">Hide</button>
<button id="show5">Show</button>

<p id="para5">
Le tantō est un couteau japonais légèrement courbe à un seul tranchant dont la taille de la lame est inférieure à 30 cm. <br/>
La structure du tantō est généralement semblable à celle du katana, à la différence près qu'il est bien plus court et souvent moins courbé.<br/>
</p>


